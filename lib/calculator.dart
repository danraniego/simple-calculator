class Calculator {

  add(double numberOne, double numberTwo) {
    return numberOne + numberTwo;
  }

  subtract(double numberOne, double numberTwo) {
    return numberOne - numberTwo;
  }

  multiply(double numberOne, double numberTwo) {
    return numberOne * numberTwo;
  }

  divide(double numberOne, double numberTwo) {
    return numberOne / numberTwo;
  }
}